class RGB {
    int red;
    int green;
    int blue;
    int grey;

    public RGB() {
    }

    public RGB(int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public RGB(int red, int green, int blue, int grey) {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.grey = grey;
    }
}
